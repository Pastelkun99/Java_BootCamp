package sec01_exam;

public class BitShiftExample {

	public static void main(String[] args) {

		System.out.println("7 << 3 = " + (7 << 3));
		System.out.println("9 >> 2 = " + (9 >> 2));

		// 위의 연산자를 시프트 연산자라고 합니다.
		// 이는 전자공학에서 주로 사용되는 것으로 일반사용자는 사용할 일이 거의 없지만,
		// 임베디드 공학이나 특정 하드웨어 공학에서 종종 쓰이는 연산자이므로 한번 구경해봅시다.
	}

}
