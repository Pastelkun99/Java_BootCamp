package sec01_exam_OverridingTest;

public class UsbMemory extends HddDisk {

	int capacity;
	int rpm;
	
	//조상클래스의 생성자를 호출하면서, 자기 자신의 멤버변수를 초기화한다.
	public UsbMemory(int c, int r) {
		super(10,50);		// 조상클래스의 매개변수있는 생성자를 호출함
		this.capacity = c;
		this.rpm = r;
	}
	
	//조상클래스의 메서드를 오버라이딩 했다.
	@Override
	public String Status() {
		return "USB 메모리 용량은 " + super.capacity + "GB이며, "
				+ "RPM은 " + super.rpm + "입니다.";
	}
}
